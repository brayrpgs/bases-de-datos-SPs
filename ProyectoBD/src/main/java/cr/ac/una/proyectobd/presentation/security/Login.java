        String password = tPassword.getText();

        //Creo que no se puede validar ni los campos en back
        if(userName.trim().isEmpty() || password.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Todos los campos son necesarios");
            return;
        }
        
        if(userName.equals("admin") && password.equals("1234")){ //Validar en BD
            dispose();
            initializerViews.createViewMain();
        } else {
            JOptionPane.showMessageDialog(null, "Usuario o contrase침a incorrectos");
        }
